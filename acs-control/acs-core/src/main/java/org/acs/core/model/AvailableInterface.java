package org.acs.core.model;

public class AvailableInterface {

	private VersionObject AvailableInterfaceKey;
	private VersionObject InterfaceType;
	private VersionObject InterfaceReference;
	public VersionObject getAvailableInterfaceKey() {
		return AvailableInterfaceKey;
	}
	public void setAvailableInterfaceKey(VersionObject availableInterfaceKey) {
		AvailableInterfaceKey = availableInterfaceKey;
	}
	public VersionObject getInterfaceType() {
		return InterfaceType;
	}
	public void setInterfaceType(VersionObject interfaceType) {
		InterfaceType = interfaceType;
	}
	public VersionObject getInterfaceReference() {
		return InterfaceReference;
	}
	public void setInterfaceReference(VersionObject interfaceReference) {
		InterfaceReference = interfaceReference;
	}
	
	
}

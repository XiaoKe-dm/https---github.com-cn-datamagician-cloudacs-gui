package org.acs.core.model;

public class WANDSLInterfaceConfigStats {

	private Total Total;
	private Showtime Showtime;
	private CurrentDay CurrentDay;
	private QuarterHour QuarterHour;
	
	
	public Total getTotal() {
		return Total;
	}
	public void setTotal(Total total) {
		Total = total;
	}
	public Showtime getShowtime() {
		return Showtime;
	}
	public void setShowtime(Showtime showtime) {
		Showtime = showtime;
	}
	public CurrentDay getCurrentDay() {
		return CurrentDay;
	}
	public void setCurrentDay(CurrentDay currentDay) {
		CurrentDay = currentDay;
	}
	public QuarterHour getQuarterHour() {
		return QuarterHour;
	}
	public void setQuarterHour(QuarterHour quarterHour) {
		QuarterHour = quarterHour;
	}
	
	
}

package org.acs.core.model;


public class DHCPConditionalServingPool {

	private VersionObject Enable;
	private VersionObject MaxAddress;
	private VersionObject MinAddress;
	private VersionObject VendorClassID;
	public VersionObject getEnable() {
		return Enable;
	}
	public void setEnable(VersionObject enable) {
		Enable = enable;
	}
	public VersionObject getMaxAddress() {
		return MaxAddress;
	}
	public void setMaxAddress(VersionObject maxAddress) {
		MaxAddress = maxAddress;
	}
	public VersionObject getMinAddress() {
		return MinAddress;
	}
	public void setMinAddress(VersionObject minAddress) {
		MinAddress = minAddress;
	}
	public VersionObject getVendorClassID() {
		return VendorClassID;
	}
	public void setVendorClassID(VersionObject vendorClassID) {
		VendorClassID = vendorClassID;
	}
	
	
	
}
